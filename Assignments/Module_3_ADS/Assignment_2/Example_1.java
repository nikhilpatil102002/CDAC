package com.starpattern;

public class Example_1 {

	public static void main(String[] args) {
		int n=3;
		
		
		for(int i=1;i<=n;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		System.out.println("================================");
		
		int n1=5;
		for(int i=1;i<=n1;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print("*");
			}
			System.out.println("");
		}
		
		System.out.println("================================");
		  		
	}
}
